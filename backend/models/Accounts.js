import mongoose from "mongoose";

const AccountSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    password: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    role: {
      type: String,
      required: true,
      default: "tourist",
    },
  },
  { timestamps: true }
);

const Account = mongoose.model("Account", AccountSchema);
export default Account;
